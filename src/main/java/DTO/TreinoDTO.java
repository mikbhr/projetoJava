
package DTO;


public class TreinoDTO {
    private String publico, nome;
    private int id, series, duracao, frequencia;
    
    
    
    /**
     * @return the publico
     */
    public String getPublico() {
        return publico;
    }

    /**
     * @param publico the publico to set
     */
    public void setPublico(String publico) {
        this.publico = publico;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the series
     */
    public int getSeries() {
        return series;
    }

    /**
     * @param series the series to set
     */
    public void setSeries(int series) {
        this.series = series;
    }

    /**
     * @return the duracao
     */
    public int getDuracao() {
        return duracao;
    }

    /**
     * @param duracao the duracao to set
     */
    public void setDuracao(int duracao) {
        this.duracao = duracao;
    }

    /**
     * @return the frequencias
     */
    public int getFrequencia() {
        return frequencia;
    }

    /**
     * @param frequencias the frequencias to set
     */
    public void setFrequencia(int frequencias) {
        this.frequencia = frequencias;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }
    
    
    
}
